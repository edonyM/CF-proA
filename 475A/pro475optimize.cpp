#include <iostream>

using namespace std;

int main() {
	string s[6];
	int k;
	cin>>k;
	s[0]="+------------------------+";
	s[1]="|#.#.#.#.#.#.#.#.#.#.#.|D|)";
	s[2]="|#.#.#.#.#.#.#.#.#.#.#.|.|";
	s[3]="|#.......................|";
	s[4]="|#.#.#.#.#.#.#.#.#.#.#.|.|)";
	s[5]="+------------------------+";
	for(int i=0;i<s[0].size ();i++) {
		for(int j=0;j<6;j++)
		{
			if(s[j][i]=='#'&&k)
				s[j][i]='O',k--;
		}
	}
	for(int i=0;i<6;i++)
		cout<<s[i]<<endl;
}
